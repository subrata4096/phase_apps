sub VerifyFileExists
{
  #-----------------------------------------------------------------------------
  # Get the parameters
  #-----------------------------------------------------------------------------
  local ($filename) = $_[0];

  #-----------------------------------------------------------------------------
  # Subroutine Local Variables
  #-----------------------------------------------------------------------------
  local($me) = "VerifyFileExists";
  local($errmsg1) = "\n\tThe following file is not readable  by you: \n\t";

  #-----------------------------------------------------------------------------
  # If the file does not exist, return -1 but don't abend
  # If the file exists, but is not readable or writeable, abend.
  #-----------------------------------------------------------------------------
  if (!-f $filename) { return(-1); }
  if (!-r $filename) { &Abend($me,$errmsg1 . $filename); }
  
  return(0);
}

1;
